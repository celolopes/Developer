     */
    public function index()
    {
        $user_id = auth()->user()->id;
        $tarefas = Tarefa::where('user_id', $user_id)->paginate(10);
        //Verificar se o usuário está autenticado
        if (auth()->check()) {
            return view('tarefa.index', ['tarefas' => $tarefas]);
        } else {
            return redirect()->route('login');
        }
    }

